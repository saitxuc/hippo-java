package com.hippo.client;

import com.hippo.network.Session;

public class ClientSessionResult {

	private Session session;
	
	private String bucketNum;
	
	public ClientSessionResult(Session session) {
		this.session = session;
	}
	
	public ClientSessionResult(Session session, String bucketNum) {
		this.session = session;
		this.bucketNum = bucketNum;
	}

	public Session getSession() {
		return session;
	}

	public String getBucketNum() {
		return bucketNum;
	}
}
