public interface Adjure {
	
	/**
	 * 
	 * @return
	 */
    String getName();
    
    /**
     * 
     * @return
     */
    String getOneLineDescription();
    
    /**
     * 
     * @param context
     */
    void setCommandContext(AdjureContext context );
    
    /**
     * 
     * @param tokens
     * @throws Exception
     */
    void execute(List<String> tokens) throws Exception;
}
