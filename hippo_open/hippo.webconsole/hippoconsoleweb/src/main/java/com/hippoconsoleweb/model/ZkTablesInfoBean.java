package com.hippoconsoleweb.model;

import java.io.Serializable;
import java.util.Date;

public class ZkTablesInfoBean implements Serializable {


	private static final long serialVersionUID = 1L;
	private long id;
	private String type;
	private String content;
	private int df;
	private Date createdate;
	private long zkClusterId;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getDf() {
		return df;
	}
	public void setDf(int df) {
		this.df = df;
	}
	public Date getCreatedate() {
		return createdate;
	}
	public void setCreatedate(Date createdate) {
		this.createdate = createdate;
	}
	public long getZkClusterId() {
		return zkClusterId;
	}
	public void setZkClusterId(long zkClusterId) {
		this.zkClusterId = zkClusterId;
	}
	
	
}
