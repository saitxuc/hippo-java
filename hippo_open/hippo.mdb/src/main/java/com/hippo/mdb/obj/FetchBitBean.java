public class FetchBitBean {
    private byte orginData;

    private byte[] expireTime;

    private int version;

    private byte[] key;

    public FetchBitBean(byte orginData, byte[] expireTime, int version, byte[] key) {
        this.version = version;
        this.key = key;
        this.orginData = orginData;
        this.expireTime = expireTime;
    }

    public byte getOrginData() {
        return orginData;
    }

    public void setOrginData(byte orginData) {
        this.orginData = orginData;
    }

    public byte[] getExpireTime() {
        return expireTime;
    }

    public void setExpireTime(byte[] expireTime) {
        this.expireTime = expireTime;
    }

    public int getVersion() {
        return version;
    }

    public void setVersion(int version) {
        this.version = version;
    }

    public byte[] getKey() {
        return key;
    }

    public void setKey(byte[] key) {
        this.key = key;
    }
}
