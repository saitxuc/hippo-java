package com.hippo.leveldb.cluster;

/**
 * @author yangxin
 */
public class LdbResult {
    private int version;
    private long expire;
    private byte[] value;

    public LdbResult(int version, long expire, byte[] value) {
        this.version = version;
        this.expire = expire;
        this.value = value;
    }

    public int getVersion() {
        return version;
    }

    public void setVersion(int version) {
        this.version = version;
    }

    public long getExpire() {
        return expire;
    }

    public void setExpire(long expire) {
        this.expire = expire;
    }

    public byte[] getValue() {
        return value;
    }

    public void setValue(byte[] value) {
        this.value = value;
    }
}
