package com.hippo.broker.cluster;

import java.util.ArrayList;
import java.util.List;


/**
 * 
 * @author saitxuc
 * 2015-3-9
 */
public class ReplicatedClientInfo implements java.io.Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 2250268233043533244L;

	private List<String> buckets = new ArrayList<String>();
	
	private String masterip;
	
	private int masterport;
	
	public ReplicatedClientInfo() {
		
	}

	public List<String> getBuckets() {
		return buckets;
	}

	public void setBuckets(List<String> buckets) {
		this.buckets = buckets;
	}

	public String getMasterip() {
		return masterip;
	}

	public void setMasterip(String masterip) {
		this.masterip = masterip;
	}

	public int getMasterport() {
		return masterport;
	}

	public void setMasterport(int masterport) {
		this.masterport = masterport;
	}
	
	
	
}
