package com.hippo.broker.cluster.command;

import com.hippo.network.command.Command;

/**
 * 
 * @author saitxuc
 * 2015-3-9
 */
public class ReplicatedBucketDataRequest extends Command {

    /**
     * 
     */
    private static final long serialVersionUID = -1528292901061390866L;

    private String bucketNo;

    private String dbinfo;

    private int offset;

    private String sizeFlag;

    private int size;

    public ReplicatedBucketDataRequest() {

    }

    public String getBucketNo() {
        return bucketNo;
    }

    public void setBucketNo(String bucketNo) {
        this.bucketNo = bucketNo;
    }

    public String getDbinfo() {
        return dbinfo;
    }

    public void setDbinfo(String dbinfo) {
        this.dbinfo = dbinfo;
    }

    public int getOffset() {
        return offset;
    }

    public void setOffset(int offset) {
        this.offset = offset;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public String getSizeFlag() {
        return sizeFlag;
    }

    public void setSizeFlag(String sizeFlag) {
        this.sizeFlag = sizeFlag;
    }

}
